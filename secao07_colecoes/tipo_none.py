"""
Tipo None:
        Representa o tipo sem tipo

Quando utilizamos?
    Podemosd utilizar None quando queremos criar uma variável
    e inicializá-la com um tipo, antes de recebermos um valor final

OBS: O tipo None e python é sempre considerado como False
"""
numeros = None
print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67

print(numeros)
print(type(numeros))
